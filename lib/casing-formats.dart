/// Accepted casing formats
enum CasingFormat {
  /// this is lower case
  lowerCase,

  /// THIS IS UPPER CASE
  upperCase,

  /// This is scentence case
  scentenceCase,

  /// This Is Title Case
  titleCase,

  /// thisIsCamelCase
  camelCase,

  /// ThisIsPascalCase
  pascalCase,

  /// this_is_snake_case
  snakeCase,

  /// THIS_IS_UPPER_SNAKE_CASE
  upperSnakeCase,

  /// This_Is_Title_Snake_Case
  titleSnakeCase,

  /// this-is-kebab-case
  kebabCase,

  /// THIS-IS-UPPER-KEBAB-CASE
  upperKebabCase,

  /// This-Is-Title-Kebab-Kase
  titleKebabCase,
}
